%% @doc Encode all applicable characters found in list or binary parameter to HTML entities
%% Support full unicode. If invalid encoding is found this method will return <<"invalid encoding">> protecting us from attackers.
encode(Term) ->

	UList   = unicode:characters_to_list(Term, utf8),
	case UList of
		{error, _, _} -> <<"invalid encoding">>;
		_ ->
			Result  = encode_parse_ulist(UList, []),
			unicode:characters_to_binary(Result, utf32, utf8)
	end.

